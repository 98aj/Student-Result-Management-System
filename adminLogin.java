    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fathername = jTextField3.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srp","root","Puff@2708");
            Statement st = con.createStatement();
            st.executeUpdate("insert into students(rollno,course,branch,name,gender,fathername) values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null, "Successfuly Updated");
            setVisible(false);
            new adminLogin().setVisible(true);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Roll Number Already Exist");
            setVisible(false);
            new adminLogin().setVisible(true);
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

